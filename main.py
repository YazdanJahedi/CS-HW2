import crypto


# Example 
inp = b'\x2b\x7e\x15\x16\x28\xae\xd2\xa6\xab\xf7\xcf\x59\x64\x25\x19\xfa\x3a\xaa\xf3\x26\x23\x1e\x21\x23\xee\xab\xcd\x52\xe4\x23\x29\x0a'
key = b'\x46\xaa\x2e\x32\x11\x12\x13\xe0\x01\x23\xa7\x31\x64\x4e\xfa\xee'

e = crypto.encrypt(inp, key)
d = crypto.decrypt(e, key)
print("encrypted:  ", e.hex())
print("decrypted:  ", d.hex())

print( "IS input EQUAL decrypted? : ", d == inp)